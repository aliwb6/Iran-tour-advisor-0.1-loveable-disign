const dicts: Record<Lang, Dict> = { en, fa, ar };

type Ctx = { lang: Lang; dir: "ltr" | "rtl"; setLang: (l: Lang) => void; t: (k: string) => string };
const I18nCtx = createContext<Ctx | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");

  useEffect(() => {
    const saved = (typeof window !== "undefined" && (localStorage.getItem("lang") as Lang)) || null;
    if (saved && dicts[saved]) setLangState(saved);
  }, []);

  useEffect(() => {
    const dir = LANGS.find((l) => l.code === lang)?.dir ?? "ltr";
    if (typeof document !== "undefined") {
      document.documentElement.lang = lang;
      document.documentElement.dir = dir;
    }
  }, [lang]);

  const setLang = (l: Lang) => {
    setLangState(l);
    if (typeof window !== "undefined") localStorage.setItem("lang", l);
  };

  const dir = LANGS.find((l) => l.code === lang)?.dir ?? "ltr";
  const t = (k: string) => dicts[lang][k] ?? dicts.en[k] ?? k;

  return <I18nCtx.Provider value={{ lang, dir, setLang, t }}>{children}</I18nCtx.Provider>;
}

export function useI18n() {
  const c = useContext(I18nCtx);
  if (!c) throw new Error("useI18n must be used inside I18nProvider");
  return c;
}
